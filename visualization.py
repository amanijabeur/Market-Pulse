# Scatter
sns.scatterplot(
    data=df,
    x="Price",
    y="% Change",
    hue="Status",
    palette={"Gainer": "#2DC653", "Loser": COLOR_BRIGHT, "Flat": "#FFC300"},
    s=80,
    edgecolor="white",
    ax=ax4
)

ax4.set_title(
    "Price vs Percentage Change",
    fontsize=13,
    fontweight="bold"
)

fig.suptitle(
    "Stock Market Dashboard",
    fontsize=22,
    fontweight="bold",
    color=COLOR_DEEP,
    y=0.97
)

fig.subplots_adjust(
    top=0.88,
    left=0.06,
    right=0.96,
    bottom=0.08
)

save_chart(
    fig,
    "08_dashboard.png"
)


# ======================================================

print(f"\nCharts saved in {PATHS.OUTPUTS_DIR}. Dashboard/report PDF generation removed so daily-changing values stay in the live dashboard.")
